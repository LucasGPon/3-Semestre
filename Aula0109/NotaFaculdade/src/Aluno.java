public class Nota {
    
    public double calcularNota(double a, double b) {
        return a + b / 2;
    }

    public double calcularNota(double a, double b, double c) {
        return (a * 1) + (b * 2) + (c * 3) / 6;
    }

    public double calcularNota(double a, double b, double c, double d) {
        return (a * 0.15) + (b * 0.30) + (c * 0.1) + (d * 0.45);
    }


}
